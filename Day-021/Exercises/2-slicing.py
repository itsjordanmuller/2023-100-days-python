print("\nSlicing Exercise\n")

# Define a list of piano keys
piano_keys = ["a", "b", "c", "d", "e", "f", "g"]

# Output keys c, d, e
print(piano_keys[2:5])

# Output keys from c to g
print(piano_keys[2:])

# Output keys from a to e
print(piano_keys[:5])

# Output every second key from b to f
print(piano_keys[1:6:2])

# Output every second key in the entire list
print(piano_keys[::2])

# Output all keys in reverse order
print(piano_keys[::-1])
